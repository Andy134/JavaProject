public class BaseUrl {
    private String baseUrlId;
    private String baseUrlLink;

    /**
     * @return the baseUrlId
     */
    public String getBaseUrlId() {
        return baseUrlId;
    }

    /**
     * @param baseUrlId the baseUrlId to set
     */
    public void setBaseUrlId(String baseUrlId) {
        this.baseUrlId = baseUrlId;
    }

    /**
     * @return the baseUrlLink
     */
    public String getBaseUrlLink() {
        return baseUrlLink;
    }

    /**
     * @param baseUrlLink the baseUrlLink to set
     */
    public void setBaseUrlLink(String baseUrlLink) {
        this.baseUrlLink = baseUrlLink;
    }
}
